OK=0
FAIL=0
SKIP=0

for rel in "${URLS[@]}"; do
  year_month=$(echo "$rel" | cut -d'/' -f1,2)
  filename=$(basename "$rel")
  dest_dir="$OUT_DIR/$year_month"
  dest_file="$dest_dir/$filename"

  if [ -f "$dest_file" ]; then
    SKIP=$((SKIP+1))
    continue
  fi

  mkdir -p "$dest_dir"
  if curl -sf --max-time 30 -o "$dest_file" "$BASE_URL/$rel"; then
    OK=$((OK+1))
    echo "OK: $rel"
  else
    FAIL=$((FAIL+1))
    echo "FAIL: $rel"
    rm -f "$dest_file"
  fi
done

echo ""
echo "=== DONE: $OK OK | $SKIP SKIP | $FAIL FAIL ==="
